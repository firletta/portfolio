const newShade = (hexColor, magnitude) => {
  hexColor = hexColor.replace(`#`, ``);
  if (hexColor.length === 6) {
      const decimalColor = parseInt(hexColor, 16);
      let r = (decimalColor >> 16) + magnitude;
      r > 255 && (r = 255);
      r < 0 && (r = 0);
      let g = (decimalColor & 0x0000ff) + magnitude;
      g > 255 && (g = 255);
      g < 0 && (g = 0);
      let b = ((decimalColor >> 8) & 0x00ff) + magnitude;
      b > 255 && (b = 255);
      b < 0 && (b = 0);
      return `#${((1 << 24) + (r << 16) + (b << 8) + g).toString(16).slice(1)}`;
  } else {
      return hexColor;
  }
};

document.getElementById('fur').addEventListener('input', function(event) {
  const color = event.target.value;
  const darkerColor = newShade(color, -20); // Darken by 20
  document.documentElement.style.setProperty('--color-fur', color);
  document.documentElement.style.setProperty('--color-furDark', darkerColor);
});

